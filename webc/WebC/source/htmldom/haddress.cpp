CSSValueType HTMLAddress::GetStyleFromHTML(CSSPropertyType property, CSSPropertyValue *value)
{
	switch (property)
	{
		case CSS_PROPERTY_FONT_STYLE:
			value->fontStyle = CSS_FONT_STYLE_ITALIC;
			return (CSS_VALUE_SPECIFIED);
						
		default:			
			break;
	}
	
	return (HTMLDiv::GetStyleFromHTML(property, value));
}

	
