CSSValueType HTMLStrike::GetStyleFromHTML(CSSPropertyType Property, CSSPropertyValue *pValue)
{
	switch (Property)
	{
		case CSS_PROPERTY_TEXT_DECORATION:
			pValue->textDecoration = CSS_TEXT_DECORATION_LINETHRU;
			return (CSS_VALUE_SPECIFIED);

		case CSS_PROPERTY_DISPLAY:
			pValue->display = CSS_DISPLAY_INLINE;
			return (CSS_VALUE_SPECIFIED);		
			
		default:
			break;
	}
	
	return (CSS_VALUE_UNKNOWN);
}


