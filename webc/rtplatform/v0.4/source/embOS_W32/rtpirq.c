/*----------------------------------------------------------------------*
                          rtp_irq_push_disable
 *----------------------------------------------------------------------*/
RTP_IRQCNTXT rtp_irq_push_disable (void)
{
        OS_IncDI();
	return ((RTP_IRQCNTXT)0);
}

/*----------------------------------------------------------------------*
                              rtp_irq_pop
 *----------------------------------------------------------------------*/
void rtp_irq_pop (RTP_IRQCNTXT previousLevel)
{
        OS_DecRI();
}

/*----------------------------------------------------------------------*
                              rtp_irq_disable
 *----------------------------------------------------------------------*/
void rtp_irq_disable (void)
{
        OS_DI();
}

/*----------------------------------------------------------------------*
                              rtp_irq_enable
 *----------------------------------------------------------------------*/
void rtp_irq_enable (void)
{
        OS_RestoreI();
}


