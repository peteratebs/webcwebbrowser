/*----------------------------------------------------------------------*
                             rtp_kern_init
 *----------------------------------------------------------------------*/
void rtp_kern_init (void)
{
  OS_IncDI();           /* Initially disable interrupts  */
  OS_InitKern();        /* initialize OS */
  OS_InitHW();          /* initialize Hardware for OS */
}

/*----------------------------------------------------------------------*
                             rtp_kern_run
 *----------------------------------------------------------------------*/
void rtp_kern_run (void)
{
  OS_Start();           /* Start multitasking */
}


