/*----------------------------------------------------------------------*
                              _rtp_malloc
 *----------------------------------------------------------------------*/
void * _rtp_malloc (unsigned int size)
{
void * ptr;

	ptr = (void*) ks_malloc(1, size, 0);
    if (!ptr)
    {
        return ((void *)0);
    }
    return (ptr);
}


/*----------------------------------------------------------------------*
                             _rtp_realloc
 *----------------------------------------------------------------------*/
void * _rtp_realloc (void * ptr, unsigned int size)
{
    ptr = (void*) ks_realloc((PFBYTE)ptr, 1, size, 0);
    if (!ptr)
    {
        return ((void *)0);
    }
    return (ptr);
}


/*----------------------------------------------------------------------*
                              _rtp_free
 *----------------------------------------------------------------------*/
void _rtp_free (void * ptr)
{
    ks_free((PFBYTE)ptr, 0, 0);
}


