/*----------------------------------------------------------------------*
                             rtp_kern_init
 *----------------------------------------------------------------------*/
void rtp_kern_init (void)
{
    /* ---------------------------------------------------------------------- */	
    /*  INITIALIZE ANY RTOS COMPONENTS NECESSARY TO RUN THIS DEMO.            */
    /* ---------------------------------------------------------------------- */    
    OSVectSet (OS_VECTOR, OSCtxSw);
    
    OSInit();
    
    AppTickInit();
    
   
}

