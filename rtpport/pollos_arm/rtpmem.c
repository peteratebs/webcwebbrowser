/*----------------------------------------------------------------------*
                              rtp_malloc
 *----------------------------------------------------------------------*/
void * _rtp_malloc (unsigned int size, int who)
{
RTP_PFINT8 ptr;

	size = ((size+ALIGN-1)/ALIGN)*ALIGN;
   
   //ptr = (PFBYTE)malloc(size);
	ptr = ((RTP_PFINT8)bget(size));
	if (!ptr)
	{
		RTP_DEBUG_ERROR("_rtp_malloc: Failed to alloc mem of size: ", EBS_INT1, size, 0);
	}
	return(ptr);

}



